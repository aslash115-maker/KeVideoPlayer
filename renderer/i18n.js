  const RTL_LOCALES = new Set(['ar']);
  const STORAGE_KEY = 'vfs.lang';
  const FALLBACK = 'en';
  const DEFAULT = 'zh-CN';

  let currentLocale = DEFAULT;
  const listeners = [];

  function format(str, vars) {
    if (!vars) return str;
    return str.replace(/\{(\w+)\}/g, (_, k) => (k in vars ? vars[k] : `{${k}}`));
  }

  const api = {
    LOCALES,
    list() {
      return Object.keys(LOCALES).map((code) => ({ code, name: LOCALES[code]._name }));
    },
    current() { return currentLocale; },
    isRTL(code) { return RTL_LOCALES.has(code || currentLocale); },
    t(key, vars) {
      const dict = LOCALES[currentLocale] || LOCALES[FALLBACK];
      const raw = dict[key] != null ? dict[key]
                : LOCALES[FALLBACK][key] != null ? LOCALES[FALLBACK][key]
                : key;
      return format(raw, vars);
    },
    set(code) {
      if (!LOCALES[code]) return;
      currentLocale = code;
      try { localStorage.setItem(STORAGE_KEY, code); } catch {}
      document.documentElement.lang = code;
      document.documentElement.dir = api.isRTL(code) ? 'rtl' : 'ltr';
      listeners.forEach((cb) => { try { cb(code); } catch {} });
    },
    onChange(cb) { listeners.push(cb); },
    init() {
      let saved = null;
      try { saved = localStorage.getItem(STORAGE_KEY); } catch {}
      api.set(saved && LOCALES[saved] ? saved : DEFAULT);
    },
  };

  window.VFSi18n = api;
